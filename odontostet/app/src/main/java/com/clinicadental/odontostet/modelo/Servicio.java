package com.clinicadental.odontostet.modelo;

public class Servicio {

    private String idServicio;
    private String nombreServicio;
    private String descServicio;
    private String imgServicio;
    private String costoServicio;

    public String getIdServicio() {
        return idServicio;
    }

    public void setIdServicio(String idServicio) {
        this.idServicio = idServicio;
    }

    public String getNombreServicio() {
        return nombreServicio;
    }

    public void setNombreServicio(String nombreServicio) {
        this.nombreServicio = nombreServicio;
    }

    public String getDescServicio() {
        return descServicio;
    }

    public void setDescServicio(String descServicio) {
        this.descServicio = descServicio;
    }

    public String getImgServicio() {
        return imgServicio;
    }

    public void setImgServicio(String imgServicio) {
        this.imgServicio = imgServicio;
    }

    public String getCostoServicio() {
        return costoServicio;
    }

    public void setCostoServicio(String costoServicio) {
        this.costoServicio = costoServicio;
    }
}
